
public class Towers {

	private int towerX;
	private int towerY;
	private int towerCost;
	
	
	
	public Towers(int towerX, int towerY, int towerCost) {
		this.towerX = towerX;
		this.towerY = towerY;
		this.towerCost = towerCost;
	}
	
	public int getTowerX() {
		return towerX;
	}
	public void setTowerX(int towerX) {
		this.towerX = towerX;
	}
	public int getTowerY() {
		return towerY;
	}
	public void setTowerY(int towerY) {
		this.towerY = towerY;
	}
	public int getTowerCost() {
		return towerCost;
	}
	public void setTowerCost(int towerCost) {
		this.towerCost = towerCost;
	}
	
	
	
}
